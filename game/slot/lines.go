package slot

import (
	"encoding/json"
	"math"
	"os"

	"gopkg.in/yaml.v3"
)

type Linex [8]Pos

func (l *Linex) At(x Pos) Pos {
	return l[x-1]
}

func (l *Linex) Set(x Pos, val Pos) {
	l[x-1] = val
}

func (l *Linex) Cover(op Linex) *Linex {
	for i, v := range op {
		if v > 0 {
			l[i] = v
		}
	}
	return l
}

func (l *Linex) Len() int {
	for i := 7; i >= 0; i-- {
		if l[i] > 0 {
			return i + 1
		}
	}
	return 0
}

func (l *Linex) Copy(pos, num Pos) (dst Linex) {
	var x1, x2 Pos
	if num >= 0 {
		x1, x2 = pos-1, pos-1+num
	} else {
		x1, x2 = pos+num, pos
	}
	copy(dst[x1:x2], l[x1:x2])
	return
}

func (l *Linex) CopyL(num Pos) (dst Linex) {
	copy(dst[:num], l[:num])
	return
}

func (l *Linex) CopyR5(num Pos) (dst Linex) {
	copy(dst[5-num:5], l[5-num:5])
	return
}

func (l Linex) MarshalJSON() ([]byte, error) {
	return json.Marshal(l[:l.Len()])
}

func ReadObj[T any](b []byte) (obj T) {
	var err error
	if err = yaml.Unmarshal(b, &obj); err != nil {
		panic(err)
	}
	return
}

func LoadObj[T any](fpath string) (obj T) {
	var b, err = os.ReadFile(fpath)
	if err != nil {
		panic(err)
	}
	return ReadObj[T](b)
}

func ReadMap[T any](b []byte) (rm map[float64]T) {
	var err error
	if err = yaml.Unmarshal(b, &rm); err != nil {
		panic(err)
	}
	return
}

func LoadMap[T any](fpath string) (rm map[float64]T) {
	var b, err = os.ReadFile(fpath)
	if err != nil {
		panic(err)
	}
	return ReadMap[T](b)
}

func FindClosest[T any](reelsmap map[float64]T, mrtp float64) (val T, rtp float64) {
	rtp = -1000 // lets to get first reels from map in any case
	for p, v := range reelsmap {
		if math.Abs(mrtp-p) < math.Abs(mrtp-rtp) {
			val, rtp = v, p
		}
	}
	return
}


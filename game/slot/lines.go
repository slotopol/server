package slot

import (
	"encoding/json"
)

const (
	// Maximum size X*Y of Screenx data array.
	// Maximum possible value of this constant is 255 (for Pos type uint8).
	ScrxSize = 40

	// Maximum number of hits on Screenx.
	// This array has +1 element to make it zero-terminated without extra check up.
	// Zero at x-coordinate means the end of hits,
	// zero at y-coordinate means whole reel.
	HitxSize = ScrxSize + 1

	// Maximum number of positions in Linex,
	// it's maximum possible width of the Screenx.
	MaxSX = 8
)

type (
	Sym uint8 // symbol type (screen content type)
	Pos uint8 // screen or line position with 1-based index or offset
)

// Hitx is array of 1-based coordinates (X, Y) on game screen with hit symbols.
// Hitx has fixed size to avoid extra memory allocations.
type Hitx [HitxSize][2]Pos

func (c *Hitx) Push(x, y Pos) {
	var i = c.Len()
	c[i][0], c[i][1] = x, y
	c[i+1][0] = 0
}

// Add points from Linex to this object.
func (c *Hitx) Add(op Linex) {
	var i = c.Len()
	for x, y := range op {
		if y > 0 {
			c[i][0], c[i][1] = Pos(x+1), y
			i++
		}
	}
	c[i][0] = 0 // on case if hitx is reused
}

func (c *Hitx) Len() int {
	for i, p := range c {
		if p[0] == 0 {
			return i
		}
	}
	return HitxSize
}

func (c *Hitx) IsZero() bool {
	return c[0][0] == 0
}

func (c *Hitx) MarshalJSON() ([]byte, error) {
	return json.Marshal(c[:c.Len()])
}

type Linex [MaxSX]Pos

// Make mirror copy of Linex.
func L2H(op Linex) (c Hitx) {
	var i int
	for x, y := range op {
		if y > 0 {
			c[i][0], c[i][1] = Pos(x+1), y
			i++
		}
	}
	return
}

func (l *Linex) At(x Pos) Pos {
	return l[x-1]
}

func (l *Linex) Set(x Pos, val Pos) {
	l[x-1] = val
}

func (l *Linex) Add(op Linex) *Linex {
	for x, y := range op {
		if y > 0 {
			l[x] = y
		}
	}
	return l
}

// Copy of chunk at given position.
func (l *Linex) HitxPos(pos, num Pos) (dst Hitx) {
	var x1, x2 Pos
	if num >= 0 {
		x1, x2 = pos-1, pos-1+num
	} else {
		x1, x2 = pos+num, pos
	}
	for x := x1; x < x2; x++ {
		dst[x][0], dst[x][1] = x+1, l[x]
	}
	return
}

// Copy of left chunk.
func (l *Linex) HitxL(num Pos) (dst Hitx) {
	for x := range num {
		dst[x][0], dst[x][1] = x+1, l[x]
	}
	return
}

// Copy of right chunk for 5-reels slot.
func (l *Linex) HitxR5(num Pos) (dst Hitx) {
	for x := 5 - num; x < 5; x++ {
		dst[x][0], dst[x][1] = x+1, l[x]
	}
	return
}

func (l *Linex) Len() int {
	for x := 7; x >= 0; x-- {
		if l[x] > 0 {
			return x + 1
		}
	}
	return 0
}

func (l *Linex) IsZero() bool {
	return l.Len() == 0
}

func (l Linex) MarshalJSON() ([]byte, error) {
	return json.Marshal(l[:l.Len()])
}

